const { fibonacci, multiplos, countWord } = require("./prueba");

test("Contar palabras", () => {
  expect(countWord("Hi how are things? How are you? Are you a developer? I am also a developer")).toEqual([
    { word: "hi", number: 1 },
    { word: "how", number: 2 },
    { word: "are", number: 3 },
    { word: "things", number: 1 },
    { word: "you", number: 2 },
    { word: "a", number: 2 },
    { word: "developer", number: 2 },
    { word: "i", number: 1 },
    { word: "am", number: 1 },
    { word: "also", number: 1 },
    ]);
});



